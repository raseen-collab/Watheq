/**
 * وثيق — قوالب الإشعارات والإنذارات المتدرّجة لتحصيل الإيجار.
 *
 * ⚠️ تنبيه قانوني مهم:
 * هذه القوالب صيغت لتكون رسمية وقريبة من متطلبات المطالبة عبر محاكم التنفيذ
 * (منصة ناجز) استنادًا إلى عقد الإيجار الموحّد ونظام التنفيذ. ومع ذلك:
 *  - يجب مراجعتها من مختص/محامٍ مرخّص قبل الاعتماد عليها رسميًّا.
 *  - التنفيذ عبر ناجز يتطلب عقدًا موحّدًا موثّقًا في «شبكة إيجار»، وقد تتغيّر
 *    الإجراءات والاشتراطات النظامية من وقت لآخر.
 *  - وثيق يوفّر أداة صياغة وأرشفة، ولا يقدّم استشارة قانونية ولا يضمن كفاية
 *    المستند للتنفيذ. المسؤولية النظامية على المؤجر/الوكيل.
 */

export type NoticeLevel = "REMINDER" | "LATE" | "FINAL";

export interface NoticeContext {
  tenantName: string;
  nationalId?: string;
  unit: string;
  propertyName: string;
  propertyAddress?: string;
  rentAmount: number;        // قيمة الدورة
  totalDue: number;          // إجمالي المتأخر
  dueDate: string;           // تاريخ الاستحقاق (ميلادي/هجري كنص)
  daysLate?: number;         // عدد أيام التأخير
  contractNo?: string;       // رقم العقد الموحّد
  landlordName: string;      // اسم المؤجر/مدير الأملاك/المكتب
  graceDays?: number;        // مهلة السداد الممنوحة
  date?: string;             // تاريخ تحرير الإشعار
}

const sar = (n: number) => new Intl.NumberFormat("en-US").format(n);

/** (1) تذكير ودّي — يُرسل قبل الاستحقاق بـ5 أيام */
export function reminderTemplate(c: NoticeContext): string {
  return `عزيزنا المستأجر ${c.tenantName} المحترم،

تحية طيبة وبعد،
نودّ تذكيركم ودّيًا بأن قيمة إيجار الوحدة رقم (${c.unit}) بعقار ${c.propertyName} وقدرها ${sar(c.rentAmount)} ريال تستحق بتاريخ ${c.dueDate} (خلال 5 أيام).

نأمل تسديدها في موعدها حرصًا على استمرار العلاقة الإيجارية بسلاسة. ولأي استفسار نحن في خدمتكم.

مع خالص التقدير،
${c.landlordName}`;
}

/** (2) تنبيه تأخير — يُرسل بعد يومين من الاستحقاق */
export function lateTemplate(c: NoticeContext): string {
  return `إشعار بتأخّر سداد الأجرة

المستأجر: ${c.tenantName}
الوحدة: (${c.unit}) — العقار: ${c.propertyName}
${c.contractNo ? `عقد الإيجار الموحّد رقم: ${c.contractNo}\n` : ""}
نفيدكم بأن قيمة الأجرة المستحقة بتاريخ ${c.dueDate} وقدرها ${sar(c.totalDue)} ريال لم تُسدَّد حتى تاريخه، وقد مضى على استحقاقها ${c.daysLate ?? 2} يومًا.

نأمل المبادرة بالسداد خلال ${c.graceDays ?? 3} أيام تفاديًا لاتخاذ الإجراءات النظامية اللاحقة وفقًا لبنود عقد الإيجار الموحّد المبرم بيننا.

${c.landlordName}
${c.date ?? ""}`;
}

/** (3) إنذار نهائي — يُرسل بعد 15 يومًا من التأخير، ويمهّد للتنفيذ عبر ناجز */
export function finalTemplate(c: NoticeContext): string {
  return `إنذار نهائي بسداد الأجرة المتأخرة

التاريخ: ${c.date ?? "____/____/144هـ"}

من: ${c.landlordName} — بصفته المؤجر/الوكيل عن مالك العقار.
إلى: المستأجر / ${c.tenantName}${c.nationalId ? `، هوية/إقامة رقم (${c.nationalId})` : ""}، شاغل الوحدة رقم (${c.unit}) بعقار ${c.propertyName}${c.propertyAddress ? ` — ${c.propertyAddress}` : ""}.

الموضوع: إنذار نهائي بسداد الأجرة المتأخرة بموجب عقد الإيجار الموحّد${c.contractNo ? ` رقم (${c.contractNo}) المسجّل في شبكة إيجار` : " المسجّل في شبكة إيجار"}.

بالإشارة إلى عقد الإيجار الموحّد المشار إليه أعلاه، فقد ترصّد بذمّتكم مبلغ ${sar(c.totalDue)} ريال قيمة أجرة متأخرة مستحقة بتاريخ ${c.dueDate}، ولم تُسدَّد رغم مرور ${c.daysLate ?? 15} يومًا على استحقاقها وسبق تنبيهكم بذلك.

وعليه، نُنذركم إنذارًا نهائيًّا بسداد كامل المبلغ المذكور خلال مدة أقصاها ${c.graceDays ?? 5} أيام من تاريخ استلامكم هذا الإنذار.

وفي حال عدم السداد خلال المهلة المحددة، فإن المؤجر يحتفظ بكامل حقوقه النظامية في اتخاذ الإجراءات المقرّرة نظامًا — بما في ذلك المطالبة بإخلاء العين المؤجرة واستيفاء الأجرة المتأخرة والتعويضات المستحقة — عن طريق التقدّم بطلب تنفيذ عقد الإيجار الموحّد لدى محكمة التنفيذ عبر منصة ناجز، استنادًا إلى نظام التنفيذ وأحكام عقد الإيجار الموحّد.

هذا إشعار وإنذار نظامي لحفظ الحقوق.

المؤجر/الوكيل: ${c.landlordName}
التوقيع: ____________________     التاريخ: ${c.date ?? "____/____/____"}`;
}

export function buildNotice(level: NoticeLevel, c: NoticeContext): string {
  if (level === "REMINDER") return reminderTemplate(c);
  if (level === "LATE") return lateTemplate(c);
  return finalTemplate(c);
}

